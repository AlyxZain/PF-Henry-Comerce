//Estado global inicial
const initialState = {
  clothing: [],
  allClothing: [],
  detail: [],
  filtered: [],
};

function rootReducer(state = initialState, action) {
  switch (action.type) {
    case 'GET_CLOTHING':

    case 'GET_CLOTHING_DETAIL':

    case 'FILTER_CLOTHING':

    case 'SEARCH_BY_NAME':

    case 'SORT_CLOTHING':

    case 'RESET_DETAIL':

    case 'RESET_CLOTHING':

    default:
      return state;
  }
}

export default rootReducer;
